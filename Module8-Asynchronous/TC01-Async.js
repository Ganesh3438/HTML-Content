// What is Synchronous and Asynchronous programming?

// Synchronous programming means it will be use a single-thread, so only one operation or program
// will run at a time.

// Synchronous is blocking - It will only send the server one request at a time and will wait for that 
// request to be answered by the server

// Asynchronous is non-blocking which means it will send multiple requests to a server at a time.



