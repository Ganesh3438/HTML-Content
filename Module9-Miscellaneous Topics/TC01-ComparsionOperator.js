// Comparsion operator : There is two comparsion operator one is double equal to (==) and 
// another one is triple equal to(===).

let a = 2;

let b = '2';

console.log(a==b)  // true

// == always check for the value  it won't check for the data type.
// === it will check both the value and data type.

console.log(a===b) // false