let numbers = [10, 20, 30, 40, 50];
console.log(numbers)

console.log("---------------------------------------------")
// push method which will add the element in the last
numbers.push(60)
console.log(numbers)

console.log("---------------------------------------------")
// unshift method which will add the element in the first
numbers.unshift(5)
console.log(numbers)

console.log("---------------------------------------------")
// pop method will delete the last element of the array
numbers.pop()
console.log(numbers)


console.log("---------------------------------------------")
// shift method will delete the first element of the array
numbers.shift()
console.log(numbers)
